package com.dawinder.googlemaps_jetpackcompose.utils

/**
 * Enum representing different map options that can be selected.
 *
 * This enum defines various map-related options that can be chosen to display different map content
 * on the main screen of the application. Each option corresponds to a specific map-related feature
 * or view, such as a simple map, map markers, custom map markers, multiple map markers, clustered
 * markers, a map scale bar, or a street view.
 */
enum class MapOptions {
    MAP_SIMPLE, MAP_MARKER, MAP_CUSTOM_INFO_WINDOW, MAP_MULTIPLE_MARKER, MAP_MARKER_CLUSTER, MAP_SCALE_BAR, MAP_STREET_VIEW
}